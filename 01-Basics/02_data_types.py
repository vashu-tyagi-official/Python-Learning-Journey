# Variables and Data Types
my_integer = 10
my_float = 3.14
my_string = "Hello, Python!"
my_boolean = True
my_none = None

print(f"Integer: {my_integer}, Type: {type(my_integer)}")
print(f"Float: {my_float}, Type: {type(my_float)}")
print(f"String: {my_string}, Type: {type(my_string)}")
print(f"Boolean: {my_boolean}, Type: {type(my_boolean)}")
print(f"None: {my_none}, Type: {type(my_none)}")

# Type conversion
num_str = "123"
num_int = int(num_str)
print(f"Converted string to int: {num_int}, Type: {type(num_int)}")